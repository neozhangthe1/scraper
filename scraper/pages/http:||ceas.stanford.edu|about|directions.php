			<h2>driving directions</h2>
			<p class="nbp" style="text-decoration:underline;">From Highway 101 North &amp; South:</p>
			<ul>
            	<li>Take the Embarcadero Road exit west toward Stanford University.</li>
				<li>Continue on Embarcadero Road west across El Camino Real. Embarcadero Road turns into Galvez Street.</li>
				<li>Follow Galvez Street past Campus Drive East. Turn right on Memorial Way.</li>
				<li>The Knight Building is located between Littlefield Center and Frost Amphitheatre, at the end of Memorial Way.</li>
            </ul>
			<p class="nbp" style="text-decoration:underline;">From Highway 280 North &amp; South:</p>
			<ul>
            	<li>Exit 280 at Page Mill Road, heading East.</li>
			<li>Turn left (north) onto El Camino Real.</li>
			<li>Turn left at Serra Street. Follow Serra Street past Campus Drive East.</li>
			<li>Make a right on Galvez Street, and then a left onto Memorial Way.</li>
			<li>The Knight Building is located between Littlefield Center and Frost Amphitheatre, at the end of Memorial Way.</li>
            </ul>
